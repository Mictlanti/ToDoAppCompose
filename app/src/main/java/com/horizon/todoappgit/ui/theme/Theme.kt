@Composable
fun ToDoAppGitTheme(
    viewModel: ToDoViewModel,
    content: @Composable () -> Unit
) {

    val theme = viewModel.state.collectAsState()
    val colorScheme = if(theme.value.darkTheme) darkScheme else lightScheme

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}